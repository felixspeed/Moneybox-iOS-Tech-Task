class ProductInfo {
    
    var label: String?
    var subLabel: String?
    var info: String?
    var subInfo: String?
    
    init(label: String? = nil, subLabel: String? = nil, info: String? = nil, subInfo: String? = nil) {
        self.label = label
        self.subLabel = subLabel
        self.info = info
        self.subInfo = subInfo
    }
}
